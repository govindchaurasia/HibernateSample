package com.interactcrm.qstats.bean;

public class AgentDetails {

	private int staffedAgents;
	private int staffedSupervisors;
	private int aux_agents;
	private int aux_supervisors;
	
	
	public AgentDetails(int staffedAgents, int staffedSupervisors,
			int aux_agents, int aux_supervisors) {
		super();
		this.staffedAgents = staffedAgents;
		this.staffedSupervisors = staffedSupervisors;
		this.aux_agents = aux_agents;
		this.aux_supervisors = aux_supervisors;
	}
	public int getStaffedAgents() {
		return staffedAgents;
	}
	public void setStaffedAgents(int staffedAgents) {
		this.staffedAgents = staffedAgents;
	}
	public int getStaffedSupervisors() {
		return staffedSupervisors;
	}
	public void setStaffedSupervisors(int staffedSupervisors) {
		this.staffedSupervisors = staffedSupervisors;
	}
	public int getAux_agents() {
		return aux_agents;
	}
	public void setAux_agents(int aux_agents) {
		this.aux_agents = aux_agents;
	}
	public int getAux_supervisors() {
		return aux_supervisors;
	}
	public void setAux_supervisors(int aux_supervisors) {
		this.aux_supervisors = aux_supervisors;
	}
	
	
}
